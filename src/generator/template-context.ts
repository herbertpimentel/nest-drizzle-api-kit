export function buildResourceTemplateContext(
  config: NormalizedApiKitConfig,
  resource: NormalizedResourceDefinition,
): ResourceTemplateContext {
  const outputPath = config.outputPath;
  const resourceDir = path.join(outputPath, resource.pluralName);
  const dtoDir = path.join(resourceDir, 'dto');
  const servicePath = path.join(resourceDir, resource.fileNames.service);
  const guardNames = Array.from(
    new Set((resource.guards.resource ?? []).map((guard) => guard.name).filter(Boolean)),
  ).sort();
  const dbProviderToken = resolveDbProviderToken(config);
  const controller = buildControllerImportConfig(resource);
  const configHooksImportAlias = '__apiKitConfigHooksSource';
  const resourceHooksImportAlias = '__apiKitResourceHooksSource';
  const configHooksAccessExpression = config.hooks
    ? rewriteRootIdentifier(config.hooks.source.accessExpression, config.hooks.source.importName, configHooksImportAlias)
    : undefined;
  const resourceHooksAccessExpression = resource.hooks
    ? rewriteRootIdentifier(resource.hooks.source.accessExpression, resource.hooks.source.importName, resourceHooksImportAlias)
    : undefined;
  const serializableResource: SerializableResourceTemplateModel = {
    ...resource,
    original: {
      ...(resource.original.route?.tag ? { route: { tag: resource.original.route.tag } } : {}),
      ...(resource.original.query?.baseQuery
        ? { query: { baseQueryName: resource.original.query.baseQuery.name } }
        : {}),
    },
  };
  const hooks = Object.fromEntries(
    (['find', 'findOne', 'create', 'update', 'delete'] as ResourceEndpointName[]).map((endpointName) => {
      const before = buildHookCalls([
        ...(config.hooks?.before.map((entry) => ({
          ...entry,
          rootAccessExpression: configHooksAccessExpression!,
        })) ?? []),
        ...(config.hooks?.endpoints[endpointName].before.map((entry) => ({
          ...entry,
          rootAccessExpression: configHooksAccessExpression!,
        })) ?? []),
        ...(resource.hooks?.before.map((entry) => ({
          ...entry,
          rootAccessExpression: resourceHooksAccessExpression!,
        })) ?? []),
        ...(resource.hooks?.endpoints[endpointName].before.map((entry) => ({
          ...entry,
          rootAccessExpression: resourceHooksAccessExpression!,
        })) ?? []),
      ]);
      const after = buildHookCalls([
        ...(resource.hooks?.endpoints[endpointName].after.map((entry) => ({
          ...entry,
          rootAccessExpression: resourceHooksAccessExpression!,
        })) ?? []),
        ...(resource.hooks?.after.map((entry) => ({
          ...entry,
          rootAccessExpression: resourceHooksAccessExpression!,
        })) ?? []),
        ...(config.hooks?.endpoints[endpointName].after.map((entry) => ({
          ...entry,
          rootAccessExpression: configHooksAccessExpression!,
        })) ?? []),
        ...(config.hooks?.after.map((entry) => ({
          ...entry,
          rootAccessExpression: configHooksAccessExpression!,
        })) ?? []),
      ]);

      return [endpointName, { before, after }];
    }),
  ) as ResourceTemplateContext['hooks'];
  const tableImport = renderImportStatement({
    importKind: resource.source.tableImportKind,
    importName: resource.source.tableImportName,
    importSourceName: resource.source.tableImportSourceName,
    importPath: toModuleImport(servicePath, path.resolve(path.dirname(resource.source.sourceFile), resource.source.tableImportSourceFile)),
  });
  const dbSchemaImportPath = config.dbSchemaSource
    ? toModuleImport(path.join(outputPath, 'common', 'db.ts'), config.dbSchemaSource.sourceFile)
    : undefined;
  const validationImport = resource.validation
    ? [
        renderImportStatement({
          importKind: resource.validation.schemaSource.importKind,
          importName: resource.validation.schemaSource.importName,
          importSourceName: resource.validation.schemaSource.importSourceName,
          importPath: toModuleImport(servicePath, resource.validation.schemaSource.sourceFile),
        }),
        `import { resolveResourceValidationSchema, validateResourceInput } from '${toModuleImport(servicePath, path.join(outputPath, 'common', 'validation.ts'))}';`,
      ].join('\n')
    : null;
  const hookImports = [
    config.hooks
      ? renderImportStatement({
          importKind: config.hooks.source.importKind,
          importName: configHooksImportAlias,
          importSourceName: config.hooks.source.importSourceName,
          importPath: toModuleImport(servicePath, config.hooks.source.sourceFile),
        })
      : '',
    resource.hooks
      ? renderImportStatement({
          importKind: resource.hooks.source.importKind,
          importName: resourceHooksImportAlias,
          importSourceName: resource.hooks.source.importSourceName,
          importPath: toModuleImport(servicePath, resource.hooks.source.sourceFile),
        })
      : '',
  ].filter(Boolean);
  const imports: ResourceTemplateContext['imports'] = {
    tableImportPath: toModuleImport(servicePath, path.resolve(path.dirname(resource.source.sourceFile), resource.source.tableImportSourceFile)),
    tableImportKind: resource.source.tableImportKind,
    tableImportName: resource.source.tableImportName,
    tableImportSourceName: resource.source.tableImportSourceName,
    tableAccessExpression: resource.source.tableAccessExpression,
    tableQueryName: resource.source.tableQueryName,
    ...(config.dbSchemaSource
      ? {
          dbSchemaImportPath: dbSchemaImportPath!,
          dbSchemaImportKind: config.dbSchemaSource.importKind,
          dbSchemaImportName: config.dbSchemaSource.importName,
          dbSchemaImportSourceName: config.dbSchemaSource.importSourceName,
          dbSchemaAccessExpression: config.dbSchemaSource.accessExpression,
        }
      : {}),
    dbProviderToken,
    commonTypesImportPath: toModuleImport(servicePath, path.join(outputPath, 'common', 'types.ts')),
    commonQueryImportPath: toModuleImport(servicePath, path.join(outputPath, 'common', 'query.ts')),
    commonHooksImportPath: toModuleImport(servicePath, path.join(outputPath, 'common', 'hooks.ts')),
    commonValidationImportPath: toModuleImport(servicePath, path.join(outputPath, 'common', 'validation.ts')),
    ...(resource.validation
      ? {
          validationImportPath: toModuleImport(
            servicePath,
            resource.validation.schemaSource.sourceFile,
          ),
          validationImportKind: resource.validation.schemaSource.importKind,
          validationImportName: resource.validation.schemaSource.importName,
          validationImportSourceName: resource.validation.schemaSource.importSourceName,
          validationAccessExpression: resource.validation.schemaSource.accessExpression,
        }
      : {}),
    ...(config.hooks
      ? {
          configHooksImportPath: toModuleImport(servicePath, config.hooks.source.sourceFile),
          configHooksImportKind: config.hooks.source.importKind,
          configHooksImportName: configHooksImportAlias,
          configHooksImportSourceName: config.hooks.source.importSourceName,
          configHooksAccessExpression: configHooksAccessExpression!,
        }
      : {}),
    ...(resource.hooks
      ? {
          resourceHooksImportPath: toModuleImport(servicePath, resource.hooks.source.sourceFile),
          resourceHooksImportKind: resource.hooks.source.importKind,
          resourceHooksImportName: resourceHooksImportAlias,
          resourceHooksImportSourceName: resource.hooks.source.importSourceName,
          resourceHooksAccessExpression: resourceHooksAccessExpression!,
        }
      : {}),
  };
  const commonDbImportPath = imports.commonTypesImportPath.replace(/\/types$/, '/db');
  const pagination = getPaginationSettings(serializableResource);
  const queryPaginationEnabled = pagination.enabled !== false;
  const queryDrizzleImports = [
    ...(queryPaginationEnabled ? ['count'] : []),
    'eq',
    ...((resource.query.sorts ?? []).length > 0 ? ['asc', 'desc'] : []),
  ];
  const queryCommonImports = [
    'buildAllowedColumnCondition',
    ...((resource.query.relations ?? []).length > 0 ? ['buildRequestedRelations'] : []),
    'GeneratedResourceQueryBase',
    ...(queryPaginationEnabled ? ['toPositiveNumber'] : []),
  ];
  const dbType = imports.dbSchemaAccessExpression
    ? 'NodePgDatabase<typeof schema>'
    : `NodePgDatabase<{ ${imports.tableQueryName}: typeof ${imports.tableAccessExpression} }>`;
  const render: ResourceTemplateContext['render'] = {
    controller: {
      guardImport: controller.hasGuardImport ? `import { ${guardNames.join(', ')} } from '${inferGuardImportPath(resource)}';` : null,
      bigIntParserMethod: controller.usesBigIntIdParser
        ? renderPartialTemplate('controller/big-int-parser-method.ejs', {}).trim()
        : null,
      methods: renderControllerMethods({ resource: serializableResource, controller }),
      summaryByEndpoint: {
        find: buildEndpointSummary(serializableResource, 'find', `Find ${serializableResource.pluralName}`),
        findOne: buildEndpointSummary(serializableResource, 'findOne', `Find ${serializableResource.singularName}`),
        create: buildEndpointSummary(serializableResource, 'create', `Create ${serializableResource.singularName}`),
        update: buildEndpointSummary(serializableResource, 'update', `Update ${serializableResource.singularName}`),
        delete: buildEndpointSummary(serializableResource, 'delete', `Delete ${serializableResource.singularName}`),
      },
    },
    query: {
      tableImport,
      schemaImport: imports.dbSchemaAccessExpression ? `import { schema } from '${commonDbImportPath}';` : null,
      paginationImport: queryPaginationEnabled ? `import type { PaginatedResult } from '${imports.commonTypesImportPath}';` : null,
      commonQueryImport: `import {\n  ${queryCommonImports.join(',\n  ')},\n  type ParsedFilter,\n} from '${imports.commonQueryImportPath}';`,
      findManyMethod: renderQueryFindManyMethod({ resource: serializableResource, imports }),
      findOneMethod: renderQueryFindOneMethod({ resource: serializableResource, imports }),
      resolveConditionMethod: renderResolveConditionMethod({ resource: serializableResource, imports }),
      resolveOrderByMethod: renderResolveOrderByMethod({ resource: serializableResource, imports }),
      drizzleImports: queryDrizzleImports.join(', '),
      dbType,
    },
    service: {
      tableImport,
      commonDbImport: `import { ${imports.dbSchemaAccessExpression ? 'DB_PROVIDER_TOKEN, schema' : 'DB_PROVIDER_TOKEN'} } from '${commonDbImportPath}';`,
      paginationImport: pagination.enabled !== false ? `import type { PaginatedResult } from '${imports.commonTypesImportPath}';` : null,
      validationImport,
      hooksImport: hookImports.length > 0 ? `${hookImports.join('\n')}\nimport { resolveResourceHook } from '${imports.commonHooksImportPath}';` : null,
      validationSchemaDeclarations: renderValidationSchemaDeclarations({ imports, resource: serializableResource }),
      findMethod: renderFindMethod({ resource: serializableResource, imports, hooks }),
      findOneMethod: renderFindOneMethod({ resource: serializableResource, imports, hooks }),
      createMethod: renderCreateMethod({ resource: serializableResource, imports, hooks }),
      updateMethod: renderUpdateMethod({ resource: serializableResource, imports, hooks }),
      deleteMethod: renderDeleteMethod({ resource: serializableResource, imports, hooks }),
      dbType,
    },
  };

  return {
    generatedHeader: GENERATED_HEADER,
    resourceDir,
    dtoDir,
    targets: {
      createDto: path.join(dtoDir, resource.fileNames.createDto),
      updateDto: path.join(dtoDir, resource.fileNames.updateDto),
      findQueryDto: path.join(dtoDir, resource.fileNames.findQueryDto),
      idParamsDto: path.join(dtoDir, resource.fileNames.idParamsDto),
      responseDto: path.join(dtoDir, resource.fileNames.responseDto),
      metadata: path.join(resourceDir, resource.fileNames.metadata),
      service: path.join(resourceDir, resource.fileNames.service),
      query: path.join(resourceDir, resource.fileNames.query),
      controller: path.join(resourceDir, resource.fileNames.controller),
      module: path.join(resourceDir, resource.fileNames.module),
      index: path.join(resourceDir, 'index.ts'),
    },
    resource: serializableResource,
    guards: {
      names: guardNames,
      importPath: inferGuardImportPath(resource),
    },
    controller,
    ...(hooks ? { hooks } : {}),
    imports,
    render,
  };
}

function toModuleImport(fromFile: string, toFile: string): string {
  const relativePath = path
    .relative(path.dirname(fromFile), toFile)
    .replace(/\\/g, '/')
    .replace(/\.(ts|tsx|js|jsx|mjs|cjs)$/i, '');
  return relativePath.startsWith('.') ? relativePath : `./${relativePath}`;
}

function rewriteRootIdentifier(expression: string, from: string, to: string): string {
  return expression.replace(new RegExp(`^${from}(?=\\.|$)`), to);
}

export function buildRootTemplateContext(config: NormalizedApiKitConfig): RootTemplateContext {
  const commonTypesPath = path.join(config.outputPath, 'common', 'types.ts');
  const commonDbPath = path.join(config.outputPath, 'common', 'db.ts');
  const schemaImportAlias = '__apiKitSchemaSource';

  return {
    generatedHeader: GENERATED_HEADER,
    outputPath: config.outputPath,
    rootModuleClassName: config.rootModuleClassName,
    rootModuleFileName: config.rootModuleFileName,
    dbProviderToken: resolveDbProviderToken(config),
    ...(config.dbSchemaSource
      ? {
          dbSchemaImportPath: toModuleImport(commonDbPath, config.dbSchemaSource.sourceFile),
          dbSchemaImportKind: config.dbSchemaSource.importKind,
          dbSchemaImportName: schemaImportAlias,
          dbSchemaImportSourceName: config.dbSchemaSource.importSourceName,
          dbSchemaAccessExpression: rewriteRootIdentifier(
            config.dbSchemaSource.accessExpression,
            config.dbSchemaSource.importName,
            schemaImportAlias,
          ),
        }
      : {}),
    ...(config.validation
      ? {
          validation: config.validation.engineSource
            ? {
                engineName: 'custom' as const,
                engineImportPath: toModuleImport(commonDbPath, config.validation.engineSource.sourceFile),
                engineImportKind: config.validation.engineSource.importKind,
                engineImportName: config.validation.engineSource.importName,
                engineImportSourceName: config.validation.engineSource.importSourceName,
                engineAccessExpression: rewriteRootIdentifier(
                  config.validation.engineSource.accessExpression,
                  config.validation.engineSource.importName,
                  config.validation.engineSource.importName,
                ),
              }
            : {
                engineName: 'zod' as const,
              },
        }
      : {}),
    targets: {
      rootModule: path.join(config.outputPath, config.rootModuleFileName),
      index: path.join(config.outputPath, 'index.ts'),
      commonDb: commonDbPath,
      commonTypes: commonTypesPath,
      commonQuery: path.join(config.outputPath, 'common', 'query.ts'),
      commonHooks: path.join(config.outputPath, 'common', 'hooks.ts'),
      commonValidation: path.join(config.outputPath, 'common', 'validation.ts'),
      commonIndex: path.join(config.outputPath, 'common', 'index.ts'),
    },
    resources: config.resources.map((resource) => ({
      pluralName: resource.pluralName,
      moduleClassName: resource.classNames.module,
    })),
  };
}
