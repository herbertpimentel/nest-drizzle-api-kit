function renderControllerMethods(context: {
  resource: SerializableResourceTemplateModel;
  controller: ControllerImportConfig;
}): string {
  const r = context.resource;
  const controller = context.controller;
  const sections: string[] = [];

  if (r.endpoints.find.enabled) {
    sections.push(renderPartialTemplate('controller/find-method.ejs', {
      r,
      summary: buildEndpointSummary(r, 'find', `Find ${r.pluralName}`),
    }).trim());
  }

  if (r.endpoints.findOne.enabled) {
    const signature = controller.usesNumberIdPipe
      ? `async findOne(@Param('id', ParseIntPipe) id: number, @Query() query: ${r.classNames.findQueryDto}) {`
      : controller.usesBigIntIdParser
        ? `async findOne(@Param('id') id: string, @Query() query: ${r.classNames.findQueryDto}) {`
        : `async findOne(@Param() params: ${r.classNames.idParamsDto}, @Query() query: ${r.classNames.findQueryDto}) {`;
    const call = controller.usesNumberIdPipe
      ? '  return this.service.findOne(id, query);'
      : controller.usesBigIntIdParser
        ? '  return this.service.findOne(this.parseBigIntId(id), query);'
        : '  return this.service.findOne(params.id, query);';
    sections.push(renderPartialTemplate('controller/find-one-method.ejs', {
      r,
      summary: buildEndpointSummary(r, 'findOne', `Find ${r.singularName}`),
      signature,
      call,
    }).trim());
  }

  if (r.endpoints.create.enabled) {
    sections.push(renderPartialTemplate('controller/create-method.ejs', {
      r,
      summary: buildEndpointSummary(r, 'create', `Create ${r.singularName}`),
    }).trim());
  }

  if (r.endpoints.update.enabled) {
    const signature = controller.usesNumberIdPipe
      ? `async update(@Param('id', ParseIntPipe) id: number, @Body() body: ${r.classNames.updateDto}) {`
      : controller.usesBigIntIdParser
        ? `async update(@Param('id') id: string, @Body() body: ${r.classNames.updateDto}) {`
        : `async update(@Param() params: ${r.classNames.idParamsDto}, @Body() body: ${r.classNames.updateDto}) {`;
    const call = controller.usesNumberIdPipe
      ? '  return this.service.update(id, body);'
      : controller.usesBigIntIdParser
        ? '  return this.service.update(this.parseBigIntId(id), body);'
        : '  return this.service.update(params.id, body);';
    sections.push(renderPartialTemplate('controller/update-method.ejs', {
      r,
      summary: buildEndpointSummary(r, 'update', `Update ${r.singularName}`),
      signature,
      call,
    }).trim());
  }

  if (r.endpoints.delete.enabled) {
    const signature = controller.usesNumberIdPipe
      ? `async delete(@Param('id', ParseIntPipe) id: number) {`
      : controller.usesBigIntIdParser
        ? `async delete(@Param('id') id: string) {`
        : `async delete(@Param() params: ${r.classNames.idParamsDto}) {`;
    const call = controller.usesNumberIdPipe
      ? '  await this.service.delete(id);'
      : controller.usesBigIntIdParser
        ? '  await this.service.delete(this.parseBigIntId(id));'
        : '  await this.service.delete(params.id);';
    sections.push(renderPartialTemplate('controller/delete-method.ejs', {
      r,
      summary: buildEndpointSummary(r, 'delete', `Delete ${r.singularName}`),
      signature,
      call,
    }).trim());
  }

  let output = '';
  for (const section of sections) {
    const startsCreate = section.startsWith('@Post()');
    const startsDelete = section.startsWith("@Delete(':id')");
    if (output.length > 0) {
      output += startsCreate || startsDelete ? '\n\n\n\n' : '\n\n\n';
    }
    output += section;
  }

  return output.length > 0 ? indentLines(output, 2) : '';
}

function renderQueryFindManyMethod(context: {
  resource: SerializableResourceTemplateModel;
  imports: ResourceTemplateContext['imports'];
}): string {
  const sorts = context.resource.query.sorts ?? [];
  const relations = context.resource.query.relations ?? [];
  const pagination = getPaginationSettings(context.resource);
  const responseDto = context.resource.classNames.responseDto;
  return renderPartialTemplate('query/find-many-method.ejs', {
    context,
    sorts,
    relations,
    pagination,
    paginationEnabled: pagination.enabled !== false,
    responseDto,
    returnType: pagination.enabled !== false ? `PaginatedResult<${responseDto}>` : `${responseDto}[]`,
    relationNamesLiteral: relations.map((relation) => `'${relation.name}'`).join(', '),
    defaultSortsLiteral: sorts
      .filter((sort) => sort.defaultOrder)
      .map((sort) => `${sort.defaultOrder === 'desc' ? 'desc' : 'asc'}(${context.imports.tableAccessExpression}.${sort.field})`)
      .join(', '),
  }).trimEnd();
}

