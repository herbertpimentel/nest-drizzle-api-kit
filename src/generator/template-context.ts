function indentLines(value: string, spaces: number): string {
  const prefix = ' '.repeat(spaces);
  return value
    .split('\n')
    .map((line) => (line.length > 0 ? `${prefix}${line}` : line))
    .join('\n');
}

function renderPartialTemplate(
  templatePath: string,
  locals: Record<string, unknown>,
): string {
  const template = fs.readFileSync(path.join(PARTIALS_DIR, templatePath), 'utf8');
  return ejs.render(template, locals);
}

function renderImportStatement(options: {
  importKind?: 'default' | 'named' | 'namespace';
  importName?: string;
  importSourceName?: string;
  importPath?: string;
}): string {
  const { importKind, importName, importSourceName, importPath } = options;
  if (!importKind || !importName || !importPath) {
    return '';
  }

  if (importKind === 'default') {
    return `import ${importName} from '${importPath}';`;
  }

  if (importKind === 'namespace') {
    return `import * as ${importName} from '${importPath}';`;
  }

  if (importName !== importSourceName) {
    return `import { ${importSourceName} as ${importName} } from '${importPath}';`;
  }

  return `import { ${importName} } from '${importPath}';`;
}

function getPaginationSettings(resource: SerializableResourceTemplateModel) {
  const pagination = resource.query.pagination;
  if (pagination && typeof pagination === 'object') {
    return pagination;
  }

  return {
    enabled: pagination !== false,
    defaultPage: 1,
    defaultPageSize: 20,
    maxPageSize: 100,
  };
}

function inferGuardImportPath(resource: NormalizedResourceDefinition): string | null {
  const candidate = resource.original.route?.tag;
  return candidate && candidate.startsWith('.') ? candidate : null;
}

function uniquifyHookCallNames(
  entries: Array<{ suggestedName: string }>,
): string[] {
  const counts = new Map<string, number>();

  return entries.map(({ suggestedName }) => {
    const nextCount = (counts.get(suggestedName) ?? 0) + 1;
    counts.set(suggestedName, nextCount);
    return nextCount === 1 ? suggestedName : `${suggestedName}${nextCount}`;
  });
}

function buildHookCalls(
  entries: Array<NormalizedHookCallDefinition & { rootAccessExpression: string }>,
): Array<{
  constName: string;
  resolveExpression: string;
  description?: string;
}> {
  const names = uniquifyHookCallNames(entries);

  return entries.map((entry, index) => ({
    constName: names[index]!,
    resolveExpression: `${entry.rootAccessExpression}${entry.accessor}`,
    ...(entry.description ? { description: entry.description } : {}),
  }));
}

function getEndpointHooks(
  hooks: ResourceTemplateContext['hooks'] | undefined,
  endpoint: ResourceEndpointName,
): NonNullable<ResourceTemplateContext['hooks']>[ResourceEndpointName] {
  return hooks?.[endpoint] ?? { before: [], after: [] };
}

function hasEndpointHooks(
  hooks: ResourceTemplateContext['hooks'] | undefined,
  endpoint: ResourceEndpointName,
): boolean {
  const entry = getEndpointHooks(hooks, endpoint);
  return entry.before.length > 0 || entry.after.length > 0;
}

function renderHookResolutionLines(
  hooks: ResourceTemplateContext['hooks'] | undefined,
  endpoint: ResourceEndpointName,
): string[] {
  const entry = getEndpointHooks(hooks, endpoint);
  return [
    ...entry.before.map((hook) => `const ${hook.constName} = resolveResourceHook(${hook.resolveExpression});`),
    ...entry.after.map((hook) => `const ${hook.constName} = resolveResourceHook(${hook.resolveExpression});`),
  ];
}

function renderHookCallLines(
  hooks: ResourceTemplateContext['hooks'] | undefined,
  endpoint: ResourceEndpointName,
  phase: 'before' | 'after',
  indent = 2,
): string[] {
  const entry = getEndpointHooks(hooks, endpoint);
  return entry[phase].flatMap((hook) => {
    const lines: string[] = [];
    if (hook.description) {
      lines.push(`${' '.repeat(indent)}// ${hook.description}`);
    }
    lines.push(`${' '.repeat(indent)}await ${hook.constName}(context);`);
    return lines;
  });
}

function renderValidationSchemaDeclarations(context: Pick<ResourceTemplateContext, 'imports' | 'resource'>): string | null {
  if (!context.imports.validationAccessExpression) {
    return null;
  }

  const entries: Array<[ResourceEndpointName, string]> = [
    ['find', 'findValidationSchema'],
    ['findOne', 'findOneValidationSchema'],
    ['create', 'createValidationSchema'],
    ['update', 'updateValidationSchema'],
    ['delete', 'deleteValidationSchema'],
  ];

  const enabledEntries = entries
    .filter(([endpoint]) => context.resource.endpoints[endpoint].enabled)
    .map(([endpoint, constName]) => ({
      endpoint,
      constName,
    }));

  if (enabledEntries.length === 0) {
    return null;
  }

  return renderPartialTemplate('service/validation-schema-declarations.ejs', {
    validationAccessExpression: context.imports.validationAccessExpression,
    entries: enabledEntries,
  }).trim();
}

function renderFindMethod(context: {
  resource: SerializableResourceTemplateModel;
  imports: ResourceTemplateContext['imports'];
  hooks: ResourceTemplateContext['hooks'] | undefined;
}): string {
  const hasValidation = Boolean(context.imports.validationImportPath);
  const validationEnabled = hasValidation && context.resource.endpoints.find.enabled;
  const pagination = getPaginationSettings(context.resource);
  const findResultType = pagination.enabled !== false
    ? `PaginatedResult<${context.resource.classNames.responseDto}>`
    : `${context.resource.classNames.responseDto}[]`;
  return renderPartialTemplate('service/find-method.ejs', {
    context,
    findResultType,
    validationEnabled,
    hasHooks: hasEndpointHooks(context.hooks, 'find'),
    hookResolutionLines: renderHookResolutionLines(context.hooks, 'find'),
    beforeHookLines: renderHookCallLines(context.hooks, 'find', 'before'),
    afterHookLines: renderHookCallLines(context.hooks, 'find', 'after'),
  }).trimEnd();
}

