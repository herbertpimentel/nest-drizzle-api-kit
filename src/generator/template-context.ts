function renderPartialTemplate(templatePath: string, locals: Record<string, unknown>): string {
  const template = fs.readFileSync(path.join(PARTIALS_DIR, templatePath), 'utf8');
  return ejs.render(template, locals);
}

function joinBlocks(blocks: Array<string | null | undefined>, separator = '\n\n'): string {
  return blocks.filter((block): block is string => Boolean(block && block.trim().length > 0)).join(separator);
}

function quote(value: string): string {
  return JSON.stringify(value);
}

function toModuleImport(fromFile: string, toFile: string): string {
  const relativePath = path
    .relative(path.dirname(fromFile), toFile)
    .replace(/\\/g, '/')
    .replace(/\.(ts|tsx|js|jsx|mjs|cjs)$/i, '');
  return relativePath.startsWith('.') ? relativePath : `./${relativePath}`;
}

function rewriteRootIdentifier(expression: string, from: string, to: string): string {
  return expression.replace(new RegExp(`^${from}(?=\\.|$)`), to);
}

function uniqueName(baseName: string, usedNames: Set<string>): string {
  const normalizedBase = /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(baseName) ? baseName : 'value';
  let candidate = normalizedBase;
  let index = 2;

  while (usedNames.has(candidate)) {
    candidate = `${normalizedBase}${index}`;
    index += 1;
  }

  usedNames.add(candidate);
  return candidate;
}

function renderImportStatement(entry: ImportEntry): string {
  if (entry.importKind === 'default') {
    return `import ${entry.localName} from '${entry.importPath}';`;
  }

  if (entry.importKind === 'namespace') {
    return `import * as ${entry.localName} from '${entry.importPath}';`;
  }

  if (entry.localName !== entry.importSourceName) {
    return `import { ${entry.importSourceName} as ${entry.localName} } from '${entry.importPath}';`;
  }

  return `import { ${entry.localName} } from '${entry.importPath}';`;
}

function createImportRegistry(currentFile: string) {
  const usedNames = new Set<string>();
  const entries = new Map<string, ImportEntry>();

  function register(entry: Omit<ImportEntry, 'localName'> & { preferredLocalName: string }): string {
    const existing = entries.get(entry.key);
    if (existing) {
      return existing.localName;
    }

    const localName = uniqueName(entry.preferredLocalName, usedNames);
    entries.set(entry.key, {
      key: entry.key,
      importKind: entry.importKind,
      importPath: entry.importPath,
      importSourceName: entry.importSourceName,
      localName,
    });
    return localName;
  }

  return {
    addImportedSource(source: ImportedValueSource, preferredLocalName?: string): string {
      const importPath = toModuleImport(currentFile, source.sourceFile);
      const localName = register({
        key: `${importPath}|${source.importKind}|${source.importSourceName}|${source.importName}`,
        importKind: source.importKind,
        importPath,
        importSourceName: source.importSourceName,
        preferredLocalName: preferredLocalName ?? source.importName,
      });
      return rewriteRootIdentifier(source.accessExpression, source.importName, localName);
    },

    addDefaultFileImport(sourceFile: string, preferredLocalName: string): string {
      const importPath = toModuleImport(currentFile, sourceFile);
      return register({
        key: `${importPath}|default`,
        importKind: 'default',
        importPath,
        importSourceName: 'default',
        preferredLocalName,
      });
    },

    addNamedFileImport(sourceFile: string, importSourceName: string, preferredLocalName = importSourceName): string {
      const importPath = toModuleImport(currentFile, sourceFile);
      return register({
        key: `${importPath}|named|${importSourceName}`,
        importKind: 'named',
        importPath,
        importSourceName,
        preferredLocalName,
      });
    },

    render(): string {
      return [...entries.values()].map((entry) => renderImportStatement(entry)).join('\n');
    },
  };
}

function isGeneratedDto(definition: NormalizedDtoDefinition | undefined): definition is NormalizedGeneratedDtoDefinition {
  return Boolean(definition && definition.mode === 'generated');
}

function tableImportStatement(currentFile: string, resource: NormalizedResourceDefinition): string {
  return renderImportStatement({
    key: 'table',
    importKind: resource.source.tableImportKind,
    importPath: toModuleImport(
      currentFile,
      path.resolve(path.dirname(resource.source.sourceFile), resource.source.tableImportSourceFile),
    ),
    importSourceName: resource.source.tableImportSourceName,
    localName: resource.source.tableImportName,
  });
}

function dbTypeExpression(config: NormalizedApiKitConfig, resource: NormalizedResourceDefinition): string {
  if (config.dbSchemaSource) {
    return 'NodePgDatabase<typeof schema>';
  }

  return `NodePgDatabase<{ ${resource.source.tableQueryName}: typeof ${resource.source.tableAccessExpression} }>`;
}

function commonDbImportStatement(currentFile: string, outputPath: string, hasSchema: boolean): string {
  const importPath = toModuleImport(currentFile, path.join(outputPath, 'common', 'db.ts'));
  return hasSchema
    ? `import { DB_PROVIDER_TOKEN, schema } from '${importPath}';`
    : `import { DB_PROVIDER_TOKEN } from '${importPath}';`;
}

function validationImportStatement(currentFile: string, outputPath: string): string {
  return `import { validateResourceInput } from '${toModuleImport(currentFile, path.join(outputPath, 'common', 'validation.ts'))}';`;
}

function paginationImportStatement(currentFile: string, outputPath: string): string {
  return `import type { PaginatedResult } from '${toModuleImport(currentFile, path.join(outputPath, 'common', 'types.ts'))}';`;
}

function dtoTypeExpression(
  currentFile: string,
  dtoDir: string,
  registry: ReturnType<typeof createImportRegistry>,
  definition: NormalizedDtoDefinition,
): string {
  if (isGeneratedDto(definition)) {
    return registry.addNamedFileImport(path.join(dtoDir, definition.fileName), definition.className);
  }

  return registry.addImportedSource(definition.source);
}

function resourceIdParamsIdentity(resource: NormalizedResourceDefinition): {
  mode: 'generated';
  kind: 'params';
  className: string;
  fileName: string;
} {
  return {
    mode: 'generated',
    kind: 'params',
    className: `${pascalCase(resource.singularName)}IdParamsDto`,
    fileName: `${kebabCase(resource.singularName)}-id.params.dto.ts`,
  };
}

function usesGeneratedIdParams(resource: NormalizedResourceDefinition): boolean {
  const idType = resource.generatedDtos.idField?.tsType ?? 'string';
  if (idType === 'number' || idType === 'bigint') {
    return false;
  }

  return resource.functions.update.enabled
    || (resource.functions.findOne.enabled && isGeneratedDto(resource.functions.findOne.input))
    || (resource.functions.delete.enabled && isGeneratedDto(resource.functions.delete.input));
}

function buildGeneratedDtoContext(resource: NormalizedResourceDefinition): GeneratedDtoTemplateContext {
  return {
    ...(isGeneratedDto(resource.functions.find.input) ? { findInput: resource.functions.find.input } : {}),
    ...(isGeneratedDto(resource.functions.create.input) ? { createInput: resource.functions.create.input } : {}),
    ...(isGeneratedDto(resource.functions.update.input) ? { updateInput: resource.functions.update.input } : {}),
    ...(usesGeneratedIdParams(resource) ? { idParams: resourceIdParamsIdentity(resource) } : {}),
    ...(resource.functions.find.output && isGeneratedDto(resource.functions.find.output) ? { findOutput: resource.functions.find.output } : {}),
    ...(resource.functions.findOne.output && isGeneratedDto(resource.functions.findOne.output) ? { findOneOutput: resource.functions.findOne.output } : {}),
    ...(resource.functions.create.output && isGeneratedDto(resource.functions.create.output) ? { createOutput: resource.functions.create.output } : {}),
    ...(resource.functions.update.output && isGeneratedDto(resource.functions.update.output) ? { updateOutput: resource.functions.update.output } : {}),
  };
}

function dtoTargetPath(dtoDir: string, definition: { fileName: string } | undefined, enabled: boolean): string {
  return enabled && definition ? path.join(dtoDir, definition.fileName) : '';
}

